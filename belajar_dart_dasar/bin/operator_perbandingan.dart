void main() {
  // operator perbandingan
  var first = 10;
  var second = 20;

  print('Apakah $first lebih besar dari $second? ${first > second}'); // false
  print('Apakah $first lebih kecil dari $second? ${first < second}'); // true
  print('Apakah $first sama dengan $second? ${first == second}'); // false
  print('Apakah $first tidak sama dengan $second? ${first != second}'); // true
  print(
    'Apakah $first lebih besar atau sama dengan $second? ${first >= second}',
  ); // false
  print(
    'Apakah $first lebih kecil atau sama dengan $second? ${first <= second}',
  ); // true

  print('abe' == 'abe'); // true
}
