void main() {
  var first = 10;
  var second = 3;

  print('Penjumlahan: ${first + second}'); // 13
  print('Pengurangan: ${first - second}'); // 7
  print('Perkalian: ${first * second}'); // 30
  print('Pembagian: ${first / second}'); // 3.333333333333
  print('Pembagian Bulat: ${first ~/ second}'); // 3
  print('Sisa Bagi: ${first % second}'); // 1
}
